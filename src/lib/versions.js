/**
 * 
 * Здесь хранятся текущие версии проектов и универсальный React-компонент для отображения версии.
 * 
 * Как использовать:
 * 1. Для обновления версии проекта — нужно изменить запись в объекте `versions`:
 * 
 * 2. В React-компонентах импортировать и использовать <VersionBadge project="KEY" />, 
 *    где KEY — ключ из объекта `versions` (например, "WEB_APP", "JOURNAL" или "MEET").
 * 
 * 3. Если передать в пропс project значение, которого нет в объекте `versions`, компонент ничего не покажет.
 * 
 * 4. Формат версий — произвольный, но рекомендую придерживаться семантики:
 *    [Cтатус] [X.Y.Z]
 *    Например: "Beta 3.0.0", "Alpha 0.9.1", "Release 1.2.0"
 * 
 * Статус — стадия разработки, например:
 *  - Alpha   (ранняя разработка, нестабильно)
 *  - Beta    (бета-версия, функционал почти готов, но могут быть баги)
 *  - RC      (Release Candidate — кандидат в релизы)
 *  - Release (официальный релиз)
 *  - Hotfix  (оперативный патч)
 *  - Preview (предварительный просмотр)
 *
 * X — мажорная версия (крупные изменения, редизайн, смена архитектуры)
 * Y — минорная версия (добавлены новые функции, совместимые с прошлым)
 * Z — патч (исправления багов, мелкие улучшения)
 *
 * Пример:
 *  - Beta v3.0.0 — третья бета-версия с крупными изменениями
 *  - Release v1.2.4 — стабильный релиз с минорными функциями и несколькими багфиксами
 *  - Hotfix v1.2.5 — срочное исправление багов после релиза
 * 
 * 5. При выходе новых версий нужно обновить соответствующее значение в `versions`, чтобы все компоненты автоматически показывали актуальную информацию.
 */

import React from 'react';

export const versions = {
  MINI_APP: 'Beta v3.0.2',
  CLOUD: 'Beta v1.0.0',
  JOURNAL: 'Beta v1.0.0',
  MEET: 'Beta v1.0.0',
};

export function VersionBadge({ project }) {
  const version = versions[project];

  if (!version) return null;

  return (
    <span style={{
      backgroundColor: '#555',
      color: '#fff',
      fontSize: '12px',
      padding: '2px 8px',
      borderRadius: '12px',
      fontFamily: 'monospace',
      userSelect: 'none',
      fontWeight: '500',
      letterSpacing: '0.05em',
    }}>
      {version}
    </span>
  );
}